--------------------------------------------------------

  CREATE OR REPLACE FORCE VIEW "JYAR"."EMP_VIEW" ("ID", "PASSWORD", "NAME", "POSITION", "PROFILE", "DEPARTMENTID", "DEPARTMENTNAME") AS 
  select emp.ID,emp.password, emp.name, emp.position, emp.profile,emp.DEPARTMENTID, dept.DEPARTMENTNAME
from EMPLOYEE emp, department dept
where emp.departmentid = dept.DEPARTMENTID
